public class for2 {
    public static void main(String[] args) {

        // Exercício 2: Médio
        // Calcule e imprima a soma dos primeiros 10 números naturais.
        int contador = 0; 
        for (int i = 1; i <= 10; i++) {
            contador = contador + i;
        }
        System.out.println(contador);
    }
}
